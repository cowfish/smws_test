module.exports = mongoose => {
    const Distillery = mongoose.model(
	"distillery",
	mongoose.Schema(
	    {
		number: String,
		distillery: String,
		region: String,
		country: String,
		style: String
	    }
	)
    );

    return Distillery;
};

